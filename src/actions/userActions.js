export const lockUser = (id, option) => async (dispatch, getState) => {
  try {
    dispatch({ type: USER_LOCK_REQUEST });
    const {
      userLogin: { userInfo },
    } = getState();

    const config = {
      headers: {
        Authorization: `Bearer ${userInfo.token}`,
      },
    };
    const { data } = await axios.patch(
      `http://localhost:8080/api/admin/users/${id}`,
      option,
      config
    );
    dispatch({ type: USER_LOCK_SUCCESS, payload: data });
    dispatch({ type: USER_LOCK_RESET });
  } catch (error) {
    if (error.message) dispatch({ type: USER_LOCK_FAIL, payload: error.message });
  }
};
